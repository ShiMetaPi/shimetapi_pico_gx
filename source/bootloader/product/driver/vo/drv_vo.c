static xmedia_void vo_dev_config(xmedia_vo_dev dev, xmedia_bool resume_en)
{
    vo_dev_context *dev_ctx = XMEDIA_NULL;
    vo_intf_timing timing_info;
    xmedia_u32 bg_color;

    dev_ctx = drv_vo_get_dev_context(dev);
#ifdef xmfalcon
    //7605 cbm背景色 只会因layer csc改变，目前图形视频cbm约定是yuv域，所以不需要随intf csc属性而改变
    bg_color = rgb_to_yuv_8bit(dev_ctx->user_info.dev_config.bg_color);
#endif
#ifdef xmorca
    //7206 cbm背景色 色彩空间 需要根据端子变化
    if (dev_ctx->user_info.csc_info.color_info.color_space == XMEDIA_VIDEO_COLOR_SPACE_YUV) {
        bg_color = rgb_to_yuv_8bit(dev_ctx->user_info.dev_config.bg_color);
    } else {
        bg_color = dev_ctx->user_info.dev_config.bg_color;
    }
#endif
    hal_vo_set_dev_bgcolor(dev, bg_color);
    //由于有可能存在单独图形层显示或者视频层已使能但没送帧的情况，所以需要设置dev csc，否则在rgb端子上显示会有问题
    vo_enable_dev_csc(dev, dev_ctx->user_info.csc_info.color_info.color_space);

    //sync_timing
    timing_info.intf_sync = dev_ctx->user_info.dev_config.intf_sync;
    if (dev_ctx->user_info.dev_config.intf_sync != XMEDIA_VO_INTF_SYNC_USER) {
        timing_info.sync_timing = g_sync_timing[timing_info.intf_sync];
    } else {
        timing_info.sync_timing = dev_ctx->user_info.dev_config.user_sync_config.timing_info;
    }
    hal_vo_set_dev_intf_timing(dev, dev_ctx->dev_info.full_frame_rate,
        &(dev_ctx->user_info.dev_config.intf_config), &timing_info.sync_timing);

    //设置中断模式为帧中断，有效区计数
    hal_vo_set_int_mode(dev, VO_DEV_VTT_MODE_VTT0, VO_ISR_FIELD_FLAG_FRAME);
    hal_vo_set_int_mode(dev, VO_DEV_VTT_MODE_VTT3, VO_ISR_FIELD_FLAG_FRAME);
    dev_ctx->dev_info.vtt0_line_num = timing_info.sync_timing.vact / 2;
    hal_vo_set_dev_thd(dev, VO_DEV_VTT_MODE_VTT0, dev_ctx->dev_info.vtt0_line_num);
    dev_ctx->dev_info.vtt3_line_num = timing_info.sync_timing.vact;
    hal_vo_set_dev_thd(dev, VO_DEV_VTT_MODE_VTT3, dev_ctx->dev_info.vtt3_line_num);

    //打开中断
    hal_vo_clean_interrupt_state(VO_INT_STATE_TYPE_MASK, VO_ISR_INTERRUPT_TYPE_ALL);
    vo_set_dev_interrupt_enable(dev, XMEDIA_TRUE);

    //视频图形zorder
    hal_vo_set_dev_mixer_prio(dev, VO_MIX_PRIO_GFX);
    //动态时钟门控使能
    hal_vo_set_clock_gate_enable(XMEDIA_TRUE);

    hal_vo_set_layer_regup_enable(dev_ctx->dev_info.bind_layer, XMEDIA_TRUE);
    hal_vo_set_top_regup_enable(XMEDIA_TRUE);

    hal_vo_set_dev_intf_enable(dev, XMEDIA_TRUE);
    hal_vo_set_dev_intf_regup_enable(dev, XMEDIA_TRUE);
    return;
}

static xmedia_void vo_dev_clear_config(xmedia_vo_dev dev)
{
    vo_dev_context *dev_ctx = XMEDIA_NULL;
    vo_isr_interrupt_type int_type = VO_ISR_INTERRUPT_TYPE;
    dev_ctx = drv_vo_get_dev_context(dev);

    hal_vo_set_dev_intf_enable(dev, XMEDIA_FALSE);
    //清除中断的状态
    if (dev == XMEDIA_VO_DEV_0) {
         int_type = VO_ISR_INTERRUPT_TYPE_DHD0_VTT0 | VO_ISR_INTERRUPT_TYPE_DHD0_VTT3;
    } else if (dev == XMEDIA_VO_DEV_1) {
         int_type = VO_ISR_INTERRUPT_TYPE_DHD1_VTT0 | VO_ISR_INTERRUPT_TYPE_DHD1_VTT3;
    }

    hal_vo_clean_interrupt_state(VO_INT_STATE_TYPE_MASK, int_type);
    hal_vo_set_dev_thd(dev, VO_DEV_VTT_MODE_VTT0, VO_VTTH_WATERLINE);
    hal_vo_set_dev_thd(dev, VO_DEV_VTT_MODE_VTT3, VO_VTTH_WATERLINE2);
    vo_set_dev_interrupt_enable(dev, XMEDIA_FALSE);
    hal_vo_set_top_regup_enable(XMEDIA_TRUE);
    hal_vo_set_layer_regup_enable(dev_ctx->dev_info.bind_layer, XMEDIA_TRUE);
    hal_vo_set_dev_intf_regup_enable(dev, XMEDIA_TRUE);
    return;
}

static xmedia_void vo_dev_wait_intf_regup(xmedia_vo_dev dev)
{
    xmedia_s32 i = 0;
    if (VO_CHECK_VIRT_DEV(dev) == XMEDIA_TRUE) {
        return ;
    }

    /*
     * 等设备关闭往后执行，否则会因为时钟直接关闭，会有残留和闪烁现象
     * 超时定为50ms，一般输出时序都会大于20f/s
     */
    while (hal_vo_get_dev_intf_regup_state(dev) != 0){
        udelay(1000);
        i++;
        if (1 >= 50) {
            VO_TRACE(MODULE_DBG_ERR,"dev %d wait intf regup time beyond 50ms\n", dev);
            break;
        }
    }
    return;
}

