xmedia_s32 drv_vo_set_dev_config(xmedia_vo_dev dev, xmedia_vo_dev_config *dev_config)
{
    vo_dev_context *dev_ctx = XMEDIA_NULL;
    xmedia_s32 ret = XMEDIA_SUCCESS;
    xmedia_u32 frame_rate;
    vo_dev_cap *dev_cap = XMEDIA_NULL;

    CHECK_VO_DEV_ID_RETURN(dev);
    CHECK_VO_PTR_RETURN(dev_config);
    dev_ctx = drv_vo_get_dev_context(dev);

    ret = vo_check_dev_config(dev, dev_config);
    if (ret != XMEDIA_SUCCESS) {
        VO_TRACE(MODULE_DBG_ERR,"vo_check_dev_config error dev %d\n", dev);
        return ret;
    }
    frame_rate = dev_config->user_sync_config.frame_rate;
    if (dev_config->intf_sync == XMEDIA_VO_INTF_SYNC_USER) {
        ret = vo_check_dev_user_sync(dev, frame_rate, &(dev_config->user_sync_config.timing_info));
        if (ret != XMEDIA_SUCCESS) {
            VO_TRACE(MODULE_DBG_ERR,"error\n");
            return ret;
        }
    }

    if (dev_config->intf_sync >= XMEDIA_VO_INTF_SYNC_PAL && dev_config->intf_sync < XMEDIA_VO_INTF_SYNC_USER) {
        if (g_vo_sync_basic_info[dev_config->intf_sync].sync_index == dev_config->intf_sync) {
            dev_ctx->dev_info.full_frame_rate = g_vo_sync_basic_info[dev_config->intf_sync].frame_rate;
        } else {
            dev_ctx->dev_info.full_frame_rate = dev_cap->max_frame_rate;
        }
        dev_ctx->dev_info.vtotal = g_sync_timing[dev_config->intf_sync].vact +
            g_sync_timing[dev_config->intf_sync].vbb +
            g_sync_timing[dev_config->intf_sync].vfb;
    } else if (dev_config->intf_sync == XMEDIA_VO_INTF_SYNC_USER) {
        dev_ctx->dev_info.full_frame_rate = frame_rate;
        dev_ctx->dev_info.vtotal = dev_config->user_sync_config.timing_info.vact +
            dev_config->user_sync_config.timing_info.vbb +
            dev_config->user_sync_config.timing_info.vfb;
    } else {
    }

    vo_set_dev_max_size(dev, dev_config->intf_sync, &(dev_config->user_sync_config.timing_info));
    memcpy(&(dev_ctx->user_info.dev_config), dev_config, sizeof(xmedia_vo_dev_config));
    if(vo_set_dev_clk(dev, dev_ctx) != XMEDIA_SUCCESS) {
        VO_TRACE(MODULE_DBG_ERR,"dev %d vo_set_dev_clk err\n", dev);
        return XMEDIA_ERRCODE_NOT_PERMITTED;
    }
    if (VO_CHECK_VIRT_DEV(dev) != XMEDIA_TRUE) {
        if (vo_calc_dev_phy_clk_cfg(dev, dev_ctx->dev_info.clk_hz, &dev_ctx->user_info.dev_config.intf_config) !=
            XMEDIA_SUCCESS) {
            VO_TRACE(MODULE_DBG_ERR,"error\n");
            return XMEDIA_ERRCODE_NOT_SUPPORT;
        }
    }
    dev_ctx->config = XMEDIA_TRUE;

    return XMEDIA_SUCCESS;
}

