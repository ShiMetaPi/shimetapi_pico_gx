xmedia_s32 drv_vo_disable_dev(xmedia_vo_dev dev)
{
    vo_dev_context *dev_ctx = XMEDIA_NULL;

    CHECK_VO_DEV_ID_RETURN(dev);
    dev_ctx = drv_vo_get_dev_context(dev);

    if (dev_ctx->enable == XMEDIA_FALSE) {
        //仍然清config标识，只要调用disable dev，就需要重配dev config
        dev_ctx->config = XMEDIA_FALSE;
        return XMEDIA_SUCCESS;
    }

    vo_dev_clear_config(dev);

    dev_ctx->config = XMEDIA_FALSE;
    dev_ctx->enable = XMEDIA_FALSE;
    vo_dev_wait_intf_regup(dev);
    vo_dev_disable_intf_phy(dev);

    //判定所有dev都为false状态才去复位时钟，并且当前dev为非虚拟设备
    if ((vo_get_dev_enable_status(XMEDIA_VO_DEV_MAX) == XMEDIA_FALSE) && (VO_CHECK_VIRT_DEV(dev) != XMEDIA_TRUE)) {
        if (set_crg_clock_reset() == XMEDIA_FAILURE) {
            VO_TRACE(MODULE_DBG_ERR,"dev %d clock reset err!\n",dev);
            return XMEDIA_ERRCODE_NOT_READY;
        }
    }

    //时钟关闭只关 对应通道&接口&分频器相关的时钟
    vo_set_crg_clock_enable(dev, XMEDIA_FALSE);
    return XMEDIA_SUCCESS;
}

xmedia_s32 drv_vo_enable_dev(xmedia_vo_dev dev)
{
    vo_dev_context *dev_ctx = XMEDIA_NULL;
    xmedia_s32 ret = XMEDIA_SUCCESS;

    CHECK_VO_DEV_ID_RETURN(dev);
    dev_ctx = drv_vo_get_dev_context(dev);
    if (dev_ctx->config != XMEDIA_TRUE) {
        VO_TRACE(MODULE_DBG_ERR,"dev %d doesn't configured!\n", dev);
        return XMEDIA_ERRCODE_NOT_READY;
    }

    if (dev_ctx->enable == XMEDIA_TRUE) {
        VO_TRACE(MODULE_DBG_ERR,"dev %d has enabled! not permitted to enable again\n", dev);
        return XMEDIA_ERRCODE_NOT_PERMITTED;
    }

    ret = vo_dev_clock_en(dev);
    if (ret != XMEDIA_SUCCESS) {
        return ret;
    }
    ret = vo_dev_enable_intf_phy(dev);
    if (ret != XMEDIA_SUCCESS) {
        VO_TRACE(MODULE_DBG_ERR,"dev %d vo dev enable phy fail!\n",dev);
        return ret;
    }
    vo_dev_config(dev, XMEDIA_FALSE);

    dev_ctx->enable = XMEDIA_TRUE;

    return XMEDIA_SUCCESS;
}

