static vo_csc_type vo_get_csc_type(xmedia_video_color_descript *in, xmedia_video_color_descript *out)
{
    vo_csc_type i = 0;
    for (i = 0; i < VO_CSC_MAX; i++) {
        if ((in->color_space == g_color_to_csc_type[i].color_space_in) &&
            (in->quantify_range == g_color_to_csc_type[i].quantify_range_in) &&
            (out->color_space == g_color_to_csc_type[i].color_space_out) &&
            (out->quantify_range == g_color_to_csc_type[i].quantify_range_out)) {
            //如果in out是RGB域，则不需要匹配gamut, 只有yuv域才需要匹配gamut
            if ((in->color_space == XMEDIA_VIDEO_COLOR_SPACE_RGB) && (out->color_space == XMEDIA_VIDEO_COLOR_SPACE_RGB)) {
                return i;
            } else if ((in->color_space == XMEDIA_VIDEO_COLOR_SPACE_RGB) && (out->color_space == XMEDIA_VIDEO_COLOR_SPACE_YUV)) {
                if (out->color_gamut == g_color_to_csc_type[i].color_gamut_out) {
                    return i;
                }
            } else if ((in->color_space == XMEDIA_VIDEO_COLOR_SPACE_YUV) && (out->color_space == XMEDIA_VIDEO_COLOR_SPACE_RGB)) {
                if (in->color_gamut == g_color_to_csc_type[i].color_gamut_in) {
                    return i;
                }
            } else {
                if ((in->color_gamut == g_color_to_csc_type[i].color_gamut_in) &&
                    (out->color_gamut == g_color_to_csc_type[i].color_gamut_out)) {
                    return i;
                }
            }
        }
    }
    return VO_CSC_MAX;
}

static xmedia_void vo_cal_csc_info(xmedia_vo_layer layer, xmedia_video_color_descript *frame_color_info,
    vo_layer_csc_info *csc_info)
{
    xmedia_vo_dev_csc dev_csc;
    xmedia_vo_dev dev = layer;
    vo_csc_type video_csc_type = VO_CSC_MAX;
    vo_csc_type mix_csc_type = VO_CSC_MAX;
#ifdef xmfalcon
    xmedia_video_color_descript temp_color_info;
#endif

    drv_vo_get_dev_csc(dev, &dev_csc);

#ifdef xmfalcon
    //7605 统一在yuv域叠加
    temp_color_info.color_space = XMEDIA_VIDEO_COLOR_SPACE_YUV;
    temp_color_info.color_gamut = XMEDIA_VIDEO_COLOR_GAMUT_BT709;
    temp_color_info.quantify_range = XMEDIA_VIDEO_COLOR_FULL_RANGE;
    video_csc_type = vo_get_csc_type(frame_color_info, &temp_color_info);
    mix_csc_type = vo_get_csc_type(&temp_color_info, &dev_csc.color_info);
#endif
#ifdef xmorca
    video_csc_type = vo_get_csc_type(frame_color_info, &dev_csc.color_info);
    mix_csc_type = VO_CSC_MAX;
#endif
    if (video_csc_type == VO_CSC_MAX) {
        VO_TRACE(MODULE_DBG_ERR,"layer %d can not find the video csc_type,color_space : %d color_gamut %d quantify_range %d!\n",
            layer, frame_color_info->color_space, frame_color_info->color_gamut, frame_color_info->quantify_range);
        csc_info->video_csc_en = XMEDIA_FALSE;
    } else {
        csc_info->video_csc_en = XMEDIA_TRUE;
        csc_info->video_csc_type = video_csc_type;
    }

    if (mix_csc_type == VO_CSC_MAX) {
        VO_TRACE(MODULE_DBG_DEBUG,"layer %d can not find the mix csc_type,color_space : %d color_gamut %d quantify_range %d!\n",
            layer, dev_csc.color_info.color_space, dev_csc.color_info.color_gamut, dev_csc.color_info.quantify_range);
        csc_info->mix_csc_en = XMEDIA_FALSE;
    } else {
        csc_info->mix_csc_en = XMEDIA_TRUE;
        csc_info->mix_csc_type = mix_csc_type;
    }

    VO_TRACE(MODULE_DBG_DEBUG,"layer:%d csc_en:[%d-%d-%d] video mod : %d mix mode: %d  color_space %d \n",
        layer, csc_info->mix_csc_en,csc_info->video_csc_en,
        dev_csc.mode, csc_info->video_csc_type, csc_info->mix_csc_type,
        dev_csc.color_info.color_space);
    return;
}

