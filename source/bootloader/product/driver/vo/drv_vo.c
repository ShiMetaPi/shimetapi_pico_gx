int start_vo(unsigned int dev, xmedia_s32 screen_type)
{
    sample_vo_config vo_config;
    vo_dev_context *dev_ctx = XMEDIA_NULL;
    memset(&vo_config, 0 ,sizeof(sample_vo_config));
    if (sample_vo_get_dev_config(screen_type, &vo_config) != XMEDIA_SUCCESS) {
        VO_TRACE(MODULE_DBG_ERR, "sample_vo_get_dev_config error!\n");
        return XMEDIA_FAILURE;
    }
    dev_ctx = drv_vo_get_dev_context(dev);
    memset(dev_ctx, 0 ,sizeof(vo_dev_context));

    if (drv_vo_set_dev_config(dev, &vo_config.dev_config) != XMEDIA_SUCCESS) {
        VO_TRACE(MODULE_DBG_ERR, "drv_vo_set_dev_config error!\n");
        return XMEDIA_FAILURE;
    }

    if (drv_vo_set_dev_csc(dev, &vo_config.dev_csc) != XMEDIA_SUCCESS) {
        VO_TRACE(MODULE_DBG_ERR, "drv_vo_set_dev_csc error!\n");
        return XMEDIA_FAILURE;
    }

    if (drv_vo_enable_dev(dev) != XMEDIA_SUCCESS) {
        VO_TRACE(MODULE_DBG_ERR, "drv_vo_set_dev_config error!\n");
        return XMEDIA_FAILURE;
    }

    return XMEDIA_SUCCESS;
}

int stop_vo(unsigned int dev)
{
    drv_vo_disable_dev(dev);
    return 0;
}

int start_videolayer(unsigned int layer, unsigned long addr, unsigned int strd, xmedia_video_rect layer_rect)
{
    vo_layer_config_info config_info;
    xmedia_vo_dev dev = layer;
    memset(&config_info, 0 , sizeof(vo_layer_config_info));
    vo_set_layer_config_info(&config_info, layer, addr, strd, layer_rect);

    vo_isr_set_single_cfg(layer, &config_info);
    vo_isr_set_csc(layer, &config_info);
    hal_vo_set_layer_enable(layer, config_info.layer_enable);
    hal_vo_set_layer_regup_enable(layer, XMEDIA_TRUE);
    hal_vo_set_dev_intf_regup_enable(dev, XMEDIA_TRUE);
    return 0;
}

int stop_videolayer(unsigned int layer)
{
    xmedia_vo_dev dev = layer;
    hal_vo_set_layer_enable(layer, XMEDIA_FALSE);
    hal_vo_set_layer_regup_enable(layer, XMEDIA_TRUE);
    hal_vo_set_dev_intf_regup_enable(dev, XMEDIA_TRUE);
    return 0;
}

